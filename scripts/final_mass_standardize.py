import os
import re
import psycopg2

def get_db_url():
    current_dir = os.path.dirname(os.path.abspath(__file__))
    env_path = os.path.join(current_dir, '..', '.env')
    with open(env_path, 'r', encoding='utf-8') as f:
        content = f.read()
    match = re.search(r'^DATABASE_URL="?(.*?)"?$', content, re.M)
    url = match.group(1).replace('postgresql+psycopg2://', 'postgresql://')
    url = re.sub(r'@[^/:]+:\d+', '@127.0.0.1:5433', url)
    return url

def main():
    # 強力な一括マッピングルール
    # (キーワード, 振り分け先の大分類)
    rules = [
        # サービス業 (修理、医療、福祉、設計、広告、葬儀、団体など)
        ("%修理業", "サービス業"), ("%ホーム", "サービス業"), ("%福祉%", "サービス業"), ("%介護%", "サービス業"),
        ("%設計業", "サービス業"), ("%コンサル%", "サービス業"), ("%葬儀%", "サービス業"), ("%清掃%", "サービス業"),
        ("%広告%", "サービス業"), ("%持株会社", "サービス業"), ("%事務所", "サービス業"), ("%センター", "サービス業"),
        ("%学校%", "サービス業"), ("%教育%", "サービス業"), ("%病院%", "サービス業"), ("%組合%", "サービス業"),
        
        # 卸売・小売・飲食業 (ホテル、店、各種卸、通販など)
        ("%ホテル%", "卸売・小売・飲食業"), ("%旅館%", "卸売・小売・飲食業"), ("%小売%", "卸売・小売・飲食業"),
        ("%卸売%", "卸売・小売・飲食業"), ("%飲食店", "卸売・小売・飲食業"), ("%レストラン%", "卸売・小売・飲食業"),
        ("%販売%", "卸売・小売・飲食業"), ("%スーパー%", "卸売・小売・飲食業"),
        
        # 製造業 (印刷、製缶、加工、製作所、工場など)
        ("%印刷業", "製造業"), ("%製造%", "製造業"), ("%加工%", "製造業"), ("%製作所", "製造業"),
        ("%板金%", "製造業"), ("%化学%", "製造業"), ("%金型%", "製造業"), ("%工業", "製造業"),
        
        # 運輸・通信業 (サポート業、ソフト、IT、倉庫、運送など)
        ("%サポート業", "運輸・通信業"), ("%ソフトウェア%", "運輸・通信業"), ("%システム%", "運輸・通信業"),
        ("%通信%", "運輸・通信業"), ("%倉庫%", "運輸・通信業"), ("%運送%", "運輸・通信業"), ("%物流%", "運輸・通信業"),
        
        # 金融・保険業 (代理業、運用業、貸金、証券など)
        ("%保険%", "金融・保険業"), ("%運用業", "金融・保険業"), ("%貸金%", "金融・保険業"), ("%信用%", "金融・保険業"),
        
        # 不動産業 (賃貸、管理、土地、ビルなど)
        ("%賃貸%", "不動産業"), ("%管理業", "不動産業"), ("%ビル%", "不動産業"), ("%アパート%", "不動産業"),
        
        # 電気・ガス
        ("%発電%", "電気・ガス"), ("%給水%", "電気・ガス")
    ]

    conn = psycopg2.connect(get_db_url())
    conn.autocommit = True
    cur = conn.cursor()

    print("🔥 超一括クリーニングを開始します...\n")

    total_updated = 0
    for pattern, new_val in rules:
        cur.execute("""
            UPDATE companies 
            SET industry_large = %s 
            WHERE industry_large LIKE %s 
              AND industry_large NOT IN (
                  'サービス業', '不動産業', '卸売・小売・飲食業', '建設業', 
                  '林業', '漁業', '製造業', '農林・水産', 
                  '運輸・通信業', '金融・保険業', '鉱業', '電気・ガス'
              )
        """, (new_val, pattern))
        
        rows = cur.rowcount
        if rows > 0:
            print(f"📦 '{pattern}' -> '{new_val}' : {rows:,} 件完了")
            total_updated += rows

    print(f"\n🎉 完了！ 合計 {total_updated:,} 件が標準化されました。")
    cur.close()
    conn.close()

if __name__ == "__main__":
    main()